package com.xuanzhi.tools.transport;

public class Sprite {

	int spriteId;
	String spriteName;
	String spriteDesp;
	int x;
	int y;
	int[] moveSpriteIds;
	public int getSpriteId() {
		return spriteId;
	}
	public void setSpriteId(int spriteId) {
		this.spriteId = spriteId;
	}
	public String getSpriteName() {
		return spriteName;
	}
	public void setSpriteName(String spriteName) {
		this.spriteName = spriteName;
	}
	public String getSpriteDesp() {
		return spriteDesp;
	}
	public void setSpriteDesp(String spriteDesp) {
		this.spriteDesp = spriteDesp;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public int[] getMoveSpriteIds() {
		return moveSpriteIds;
	}
	public void setMoveSpriteIds(int[] moveSpriteIds) {
		this.moveSpriteIds = moveSpriteIds;
	} 
}
