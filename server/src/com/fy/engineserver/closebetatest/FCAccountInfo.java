package com.fy.engineserver.closebetatest;

public class FCAccountInfo {
	/**
	 * 封测账号
	 */
	private String fcAccount;
	/**
	 * 该账号获得的内测账号数量
	 */
	private int count;
	public String getFcAccount() {
		return fcAccount;
	}
	public void setFcAccount(String fcAccount) {
		this.fcAccount = fcAccount;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	
}
